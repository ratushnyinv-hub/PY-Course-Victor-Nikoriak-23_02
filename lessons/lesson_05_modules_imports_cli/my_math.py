def add(a, b):
    """Повертає суму двох чисел."""
    return a + b


def multiply(a, b):
    """Повертає добуток двох чисел."""
    return a * b


def square(x):
    """Повертає квадрат числа."""
    return x * x


def is_even(n):
    """Повертає True, якщо число парне."""
    return n % 2 == 0


# ────────────────────────────────────────────────────────────
# Цей блок виконується ТІЛЬКИ якщо запустити файл напряму:
#   python my_math.py
#
# При  import my_math  — блок ігнорується.
# ────────────────────────────────────────────────────────────
if __name__ == "__main__":

    print("=== Тестування my_math ===")
    print("add(2, 3)       =", add(2, 3))
    print("multiply(4, 5)  =", multiply(4, 5))
    print("square(7)       =", square(7))
    print("is_even(10)     =", is_even(10))
    print("is_even(7)      =", is_even(7))
    print("Всі тести пройшли! ✅")

