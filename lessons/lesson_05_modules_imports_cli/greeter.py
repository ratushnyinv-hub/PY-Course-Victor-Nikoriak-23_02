print("👋 Модуль greeter завантажено!")   # ← побічний ефект!

GREETING = "Привіт"
FAREWELL = "До побачення"


def greet(name):
    """Повертає вітання для користувача."""
    return f"{GREETING}, {name}!"


def farewell(name):
    """Повертає прощання для користувача."""
    return f"{FAREWELL}, {name}!"


def greet_loudly(name):
    """Вітає ВЕЛИКИМИ ЛІТЕРАМИ."""
    return greet(name).upper()


# ────────────────────────────────────────────────────────────
# Приклад правильного підходу: захищаємо тест-код
# ────────────────────────────────────────────────────────────
if __name__ == "__main__":
    print(greet("Аліса"))
    print(farewell("Аліса"))
    print(greet_loudly("Боб"))
