"""
calculator_tkinter.py

🧮 Простий графічний калькулятор на Python з використанням Tkinter.

У цьому прикладі використовується eval(), але з обмеженням
доступних функцій.

⚠️ ВАЖЛИВО:
Навіть такий підхід НЕ є повністю безпечним.
У реальних системах краще:
    • писати власний парсер
    • використовувати бібліотеки для безпечного обчислення виразів
"""

import tkinter as tk


# ─────────────────────────────────────────────
# Дозволені функції для eval
# ─────────────────────────────────────────────

"""
Ми створюємо словник функцій, які ДОЗВОЛЕНО використовувати.

Все інше буде заборонено.

Наприклад тут дозволено:
abs()
round()

Якщо користувач спробує використати:
__import__
open
os.system

це не спрацює.
"""

SAFE_FUNCTIONS = {
    "abs": abs,
    "round": round
}

"""
⚠️ Важливо:

Ми також відключаємо доступ до builtins.

Це робиться через:

{"__builtins__": None}

Це блокує доступ до:
__import__
open
exec
eval
і інших небезпечних функцій.
"""


# ─────────────────────────────────────────────
# Функція натискання кнопки
# ─────────────────────────────────────────────

def click(symbol):
    """
    Додає символ у поле вводу калькулятора.
    """

    entry.insert(tk.END, symbol)


# ─────────────────────────────────────────────
# Функція обчислення
# ─────────────────────────────────────────────

def calculate():
    """
    Обчислює математичний вираз.
    """

    try:

        expression = entry.get()

        """
        Використовуємо обмежений eval.

        Перший аргумент — вираз
        Другий — глобальне середовище
        Третій — локальне середовище
        """

        result = eval(
            expression,
            {"__builtins__": None},   # вимикаємо builtins
            SAFE_FUNCTIONS            # дозволені функції
        )

        entry.delete(0, tk.END)
        entry.insert(0, result)

    except:

        entry.delete(0, tk.END)
        entry.insert(0, "Error")


# ─────────────────────────────────────────────
# Функція очищення
# ─────────────────────────────────────────────

def clear():
    """
    Очищає поле вводу.
    """

    entry.delete(0, tk.END)


# ─────────────────────────────────────────────
# Створення вікна
# ─────────────────────────────────────────────

root = tk.Tk()
root.title("Calculator")


# ─────────────────────────────────────────────
# Поле вводу
# ─────────────────────────────────────────────

entry = tk.Entry(
    root,
    width=20,
    font=("Arial", 20)
)

entry.grid(row=0, column=0, columnspan=4)


# ─────────────────────────────────────────────
# Кнопки калькулятора
# ─────────────────────────────────────────────

buttons = [
    ("7",1,0),("8",1,1),("9",1,2),("/",1,3),
    ("4",2,0),("5",2,1),("6",2,2),("*",2,3),
    ("1",3,0),("2",3,1),("3",3,2),("-",3,3),
    ("0",4,0),(".",4,1),("=",4,2),("+",4,3)
]


# ─────────────────────────────────────────────
# Створення кнопок
# ─────────────────────────────────────────────

for (text, row, col) in buttons:

    action = lambda x=text: click(x)

    if text == "=":
        action = calculate

    b = tk.Button(
        root,
        text=text,
        width=5,
        height=2,
        command=action
    )

    b.grid(row=row, column=col)


# ─────────────────────────────────────────────
# Кнопка Clear
# ─────────────────────────────────────────────

clear_btn = tk.Button(
    root,
    text="Clear",
    command=clear
)

clear_btn.grid(
    row=5,
    column=0,
    columnspan=4,
    sticky="we"
)


# ─────────────────────────────────────────────
# Головний цикл програми
# ─────────────────────────────────────────────

"""
mainloop() запускає цикл обробки подій.

Програма починає:
• слухати кліки миші
• реагувати на натискання кнопок
• оновлювати інтерфейс

Без mainloop() вікно одразу закриється.
"""

root.mainloop()